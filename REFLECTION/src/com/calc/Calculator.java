package com.calc;

public class Calculator {
int add(int firstNo,int secondNo){
	return firstNo+secondNo;
}
int sub(int firstNo,int secondNo){
	return firstNo-secondNo;
}
int mult(int firstNo,int secondNo){
	return firstNo*secondNo;
}
double div(int firstNo,int secondNo){
	return firstNo/secondNo;
}


}
